// Language names for dropdown
const languageNames = {
    en: "English",
    fr: "Français",
    es: "Español",
    pt: "Português",
    ar: "العربية",
    de: "Deutsch"
};

// Current language
let currentLang = "en";

// Initialize language system
function initLanguage() {
    // Check for saved language preference
    const savedLang = localStorage.getItem("vicable-lang");
    if (savedLang && translations[savedLang]) {
        currentLang = savedLang;
    }

    // Apply translations
    applyTranslations();

    // Update language selector UI
    updateLanguageSelector();
}

// Apply translations to page
function applyTranslations() {
    const langData = translations[currentLang];
    if (!langData) return;

    // Apply to all elements with data-i18n attribute
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (langData[key]) {
            el.textContent = langData[key];
        }
    });

    // Apply to elements with data-i18n-placeholder attribute
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (langData[key]) {
            el.placeholder = langData[key];
        }
    });

    // Apply to elements with data-i18n-title attribute
    document.querySelectorAll("[data-i18n-title]").forEach(el => {
        const key = el.getAttribute("data-i18n-title");
        if (langData[key]) {
            el.title = langData[key];
        }
    });

    // Set document title
    const titleKey = document.querySelector("[data-i18n-title]")?.getAttribute("data-i18n-title");
    if (titleKey && langData[titleKey]) {
        document.title = langData[titleKey];
    }

    // Set HTML lang attribute
    document.documentElement.lang = currentLang;

    // Set direction for Arabic
    if (currentLang === "ar") {
        document.documentElement.dir = "rtl";
    } else {
        document.documentElement.dir = "ltr";
    }
}

// Update language selector UI
function updateLanguageSelector() {
    const selector = document.getElementById("language-selector");
    if (!selector) return;

    selector.value = currentLang;

    // Update button text
    const btn = selector.closest(".lang-dropdown")?.querySelector(".lang-btn-text");
    if (btn) {
        btn.textContent = languageNames[currentLang];
    }
}

// Change language
function changeLanguage(lang) {
    if (!translations[lang]) return;

    currentLang = lang;
    localStorage.setItem("vicable-lang", lang);
    applyTranslations();
    updateLanguageSelector();
}

// Create language selector HTML
function createLanguageSelector() {
    const langs = Object.keys(translations);
    let options = "";

    langs.forEach(lang => {
        options += `<option value="${lang}">${languageNames[lang]}</option>`;
    });

    return `
        <select id="language-selector" class="lang-selector" onchange="changeLanguage(this.value)">
            ${options}
        </select>
    `;
}

// Initialize on DOM ready
document.addEventListener("DOMContentLoaded", initLanguage);
